// Função para buscar um elemento no documento HTML
document.querySelector();

// ... buscando pela tag 
document.querySelector('input');

// ... buscando pelo identificador 
document.querySelector('#id-input');

// ... buscando pela classe 
document.querySelector('.class-input');

// ... buscando pelo elemento (tag) 
document.querySelector('input');

// ... buscando pela tag input que tem o type=tel
document.querySelector('input[type=tel]');

// Acessando um elemento e chamando a função play() para iniciar um audio
document.querySelector('#som').play();